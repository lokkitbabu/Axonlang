struct MlirGen { out: String }

impl MlirGen {
    fn new() -> Self { MlirGen { out: String::new() } }
    fn w(&mut self, s: impl AsRef<str>) { self.out.push_str(s.as_ref()); self.out.push('\n'); }

    fn emit_module(mut self, module: &IrModule) -> String {
        self.w("// Generated by scientific-dsl → MLIR (arith + func dialects)");
        self.w("module @scientific_dsl {");
        for func in &module.functions {
            self.emit_func(func);
        }
        if !module.main.is_empty() {
            let main_fn = IrFunction {
                name:   "__main__".to_string(),
                params: vec![],
                ret_ty: crate::ast::TypeAnnotation::F64,
                blocks: module.main.clone(),
            };
            self.emit_func(&main_fn);
        }
        self.w("}");
        self.out
    }

    fn emit_func(&mut self, func: &IrFunction) {
        let params: Vec<String> = func.params.iter()
            .map(|(n, _)| format!("%{}: f64", n))
            .collect();
        self.w(format!("  func.func @{}({}) -> f64 {{", func.name, params.join(", ")));
        for block in &func.blocks {
            self.w(format!("  ^{}:", block.label));
            for instr in &block.instructions {
                self.w(format!("    {}", mlir_instr(instr)));
            }
            self.w(format!("    {}", mlir_term(&block.terminator)));
        }
        self.w("  }");
        self.w("");
    }
}

fn mlir_val(v: &Value) -> String {
    match v {
        Value::Reg(r)      => format!("%v{}", r.0),
        Value::ConstF64(f) => format!("{}  : f64", f),
        Value::ConstI64(i) => format!("{}  : i64", i),
    }
}

fn mlir_instr(i: &Instruction) -> String {
    match i {
        Instruction::Add { dest, lhs, rhs } =>
            format!("%v{} = arith.addf {}, {} : f64", dest.0, mlir_val(lhs), mlir_val(rhs)),
        Instruction::Sub { dest, lhs, rhs } =>
            format!("%v{} = arith.subf {}, {} : f64", dest.0, mlir_val(lhs), mlir_val(rhs)),
        Instruction::Mul { dest, lhs, rhs } =>
            format!("%v{} = arith.mulf {}, {} : f64", dest.0, mlir_val(lhs), mlir_val(rhs)),
        Instruction::Div { dest, lhs, rhs } =>
            format!("%v{} = arith.divf {}, {} : f64", dest.0, mlir_val(lhs), mlir_val(rhs)),
        Instruction::Call { dest, func, args } => {
            let arg_s: Vec<_> = args.iter().map(mlir_val).collect();
            format!("%v{} = func.call @{}({}) : ({}) -> f64", dest.0, func, arg_s.join(", "),
                    args.iter().map(|_| "f64").collect::<Vec<_>>().join(", "))
        }
        Instruction::Comment(s) => format!("// {}", s),
        _ => format!("// [unimplemented mlir instr]"),
    }
}

fn mlir_term(t: &Terminator) -> String {
    match t {
        Terminator::Return(v) => format!("func.return {} : f64", mlir_val(v)),
        Terminator::Jump(b)   => format!("cf.br ^bb{}", b),
        Terminator::CondJump { cond, true_block, false_block } =>
            format!("cf.cond_br {}, ^bb{}, ^bb{}", mlir_val(cond), true_block, false_block),
        Terminator::Unreachable => "// unreachable".to_string(),
    }
}
